#include <stdio.h>

// Integer variables= int a = 3;
// Real variables=  float a = 7.8;
// Chacter variables= char = 'a';

// %d for integers
// %f for real values
// %c for characters
// \n= new line

int main()
{
    int a = 4;
    int d = 3;
    int z = 40 - 20;
    float b = 7.5;
    char c = 'u';

    printf("The value of a is %d \n", a);
    printf("The value of b is %f\n", b);
    printf("The value of c is %c\n", c);
    printf("Sum of a and d is %d\n", a + d);
    printf("Value of z is %d\n", z);
    return 0;
}